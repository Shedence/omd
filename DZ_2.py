import csv

file = 'Corp_Summary.csv'  # –§–∞–π–ª, –æ—Ç–∫—É–¥–∞ –±–µ—Ä–µ–º –¥–∞–Ω–Ω—ã–µ
depart_dic_info = {}  # –°–ª–æ–≤–∞—Ä—å —Å –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–µ–π –æ –¥–µ–ø–æ—Ä—Ç–∞–º–µ–Ω—Ç–∞—Ö
depart_dic = {}  # –°–ª–æ–≤–∞—Ä—å —Å –¥–µ–ø–∞—Ä—Ç–∞–º–µ–Ω—Ç–∞–º–∏ –∏ –∫–æ–º–∞–Ω–¥–∞–º–∏ –≤–Ω—É—Ç—Ä–∏ –Ω–µ–≥–æ
menu = '''–ß—Ç–æ–±—ã –≤—ã–≤–µ—Å—Ç–∏, –∫–∞–∫–∏–µ –∫–æ–º–∞–Ω–¥—ã –µ—Å—Ç—å –≤ –¥–µ–ø–æ—Ä—Ç–∞–º–µ–Ω—Ç–µ –Ω–∞–∂–º–∏ 1
–ß—Ç–æ–±—ã –≤—ã–≤–µ—Å—Ç–∏, –ø–æ–ª–Ω—É—é –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –¥–µ–ø–æ—Ä—Ç–∞–º–µ–Ω—Ç–µ –Ω–∞–∂–º–∏ 2
–ß—Ç–æ–±—ã —Å–æ—Ö—Ä–∞–Ω–∏—Ç—å –æ—Ç—á–µ—Ç –≤ –≤–∏–¥–µ csv —Ñ–∞–π–ª–∞ –Ω–∞–∂–º–∏ 3
'''


def prepare():
    """
    –§—É–Ω–∫—Ü–∏—è, —Å–æ–∑–¥–∞–Ω–Ω–∞—è –¥–ª—è —Ç–æ–≥–æ, —á—Ç–æ–±—ã —Å–º–æ–¥–µ–ª–∏—Ä–æ–≤–∞—Ç—å –¥–∞–Ω–Ω—ã–µ –¥–ª—è –≤—ã–≤–æ–¥–∞
    –≤ —É–¥–æ–±–Ω–æ–º –∏ —á–∏—Ç–∞–µ–º–æ–º —Ñ–æ—Ä–º–∞—Ç–µ –¥–ª—è –Ω–∞—à–µ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è.
    –£ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –µ—Å—Ç—å —Ç–æ–ª—å–∫–æ –¥–æ—Å—Ç—É–ø –¥–ª—è –ø—Ä–æ—Å–º–æ—Ç—Ä–∞ –¥–∞–Ω–Ω—ã—Ö, —Ç–∞–∫ —á—Ç–æ
    –Ω–∞ –≤—Ö–æ–¥ –≤ —Ñ—É–Ω–∫—Ü–∏–∏ –Ω–∏—á–µ–≥–æ –Ω–µ –ø–µ—Ä–µ–¥–∞–µ—Ç—Å—è, –ø–æ—ç—Ç–æ–º—É
    –∞–Ω–Ω–æ—Ç–∞—Ü–∏—è —Ç–∏–ø–æ–≤ –º–Ω–µ —Ç—É—Ç –Ω–µ –ø—Ä–∏–≥–æ–¥–∏—Ç—Å—è
    """
    with open(file, encoding='utf-8') as f:  # C—á–∏—Ç—ã–≤–∞–µ–º —Ñ–∞–π–ª –≤ –∫–æ–¥–∏—Ä–æ–≤–∫–µ utf-8
        csv_reader = csv.reader(f, delimiter=';')  # –†–∞–∑–¥–µ–ª—è–µ–º –º–µ–∂–¥—É —Å–æ–±–æ–π —Å—Ç–æ–±–ª—Ü—ã by ;.
        for row in csv_reader:
            if row[0] != '–§–ò–û –ø–æ–ª–Ω–æ—Å—Ç—å—é':  # –ü–µ—Ä–≤—É—é —Å—Ç—Ä–æ—á–∫—É –Ω–∞–¥–æ –ø—Ä–æ–ø—É—Å—Ç–∏—Ç—å, —Ç.–∫ –æ–Ω–∞ –Ω–µ —Å–æ–¥–µ—Ä–∂–∏—Ç –∏–Ω—Ñ–æ—Ä–º–∞—Ç–∏–≤–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
                departament = row[1]
                division = row[2]
                salary = row[-1]
                salary = int(salary)  # –ü—Ä–∏–≤–æ–¥–∏–º –∑–∞—Ä–ø–ª–∞—Ç—É –∫ –∏–Ω—Ç—É
                if departament not in depart_dic:  # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ –Ω–æ–≤—ã–π —Å—á–∏—Ç—ã–≤–∞–µ–º—ã–π –¥–µ–ø–∞—Ä—Ç–∞–º–µ–Ω—Ç —É–∂–µ –≤ –Ω–∞—à–µ–º —Å–ª–æ–≤–∞—Ä–µ
                    depart_dic[departament] = [
                        division]  # –ï—Å–ª–∏ –Ω–µ—Ç, —Ç–æ –¥–æ–±–∞–≤–ª—è–µ–º –µ–≥–æ –≤ —Å–ª–æ–≤–∞—Ä—å, –∫–∞–∫ –∫–ª—é—á, –∞ –∑–Ω–∞—á–µ–Ω–∏—è —É –∫–ª—é—á–∞ - –º–∞—Å—Å–∏–≤ –µ–≥–æ –æ—Ç–¥–µ–ª–æ–≤
                elif division not in depart_dic[departament]:
                    depart_dic[departament].append(
                        division)  # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å–ª–∏ —É –¥–µ–ø–∞—Ä—Ç–∞–º–µ–Ω—Ç–∞ —É–∂–µ –¥–∞–Ω–Ω—ã–π –æ—Ç–¥–µ–ª, –µ—Å–ª–∏ –Ω–µ—Ç —Ç–æ –¥–æ–±–∞–≤–ª—è–µ–º
                if departament not in depart_dic_info:  # –ê–Ω–∞–ª–æ–≥–∏—á–Ω–æ —Å –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–µ–π –æ –¥–µ–ø–∞—Ä—Ç–∞–º–µ–Ω—Ç–∞—Ö
                    depart_dic_info[departament] = {'number': 1, 'max_salary': salary, 'min_salary': salary,
                                                    'sum_salary': salary}  # –•—Ä–∞–Ω–∏—Ç—å –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –¥–µ–ø–∞—Ä—Ç–∞–º–µ–Ω—Ç–∞—Ö –±—É–¥–µ–º –≤ –≤–∏–¥–µ —Å–ª–æ–≤–∞—Ä—å –≤ —Å–ª–æ–≤–∞—Ä–µ
                else:
                    depart_dic_info[departament]['number'] += 1  # –î–æ–±–∞–≤–ª—è–µ–º –æ–¥–Ω–æ–≥–æ —á–µ–ª–æ–≤–µ–∫–∞
                    depart_dic_info[departament]['max_salary'] = max(depart_dic_info[departament]['max_salary'],
                                                                     salary)  # –ë–µ—Ä–µ–º –º–∞–∫—Å–∏–º—É–º –∏–∑ —Å—Ç–∞—Ä–æ–π –º–∞–∫—Å. –∑–∞—Ä–ø–ª–∞—Ç—ã –∏ –Ω–æ–≤–æ—ã–π
                    depart_dic_info[departament]['min_salary'] = min(depart_dic_info[departament]['min_salary'],
                                                                     salary)  # –ê–Ω–∞–ª–æ–≥–∏—á–Ω–æ
                    depart_dic_info[departament]['sum_salary'] = depart_dic_info[departament]['sum_salary'] + salary


def press_one():
    """
    –í—ã–≤–æ–¥–∏—Ç –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ–± –æ—Ç–¥–µ–ª–∞—Ö –≤ –¥–µ–ø–∞—Ä—Ç–∞–º–µ–Ω—Ç–µ,
    –µ—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–∞–∂–∞–ª 1
    """
    for depart in depart_dic:
        departments = ', '.join(depart_dic[depart])  # –î–∂–æ–∏–Ω–∏–º –Ω–∞—à–∏ –æ—Ç–¥–µ–ª—ã –¥–ª—è –∫—Ä–∞—Å–∏–≤–æ–≥–æ –≤—ã–≤–æ–¥–∞
        print(f'–î–µ–ø–∞—Ä—Ç–∞–º–µ–Ω—Ç: {depart} —Å–æ–¥–µ—Ä–∂–∏—Ç –æ—Ç–¥–µ–ª—ã: {departments}\n')


def press_two():
    """
    –í—ã–≤–æ–¥–∏—Ç –¥–µ—Ç–∞–ª—å–Ω—É—é –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –¥–µ–ø–æ—Ä—Ç–∞–º–µ–Ω—Ç–∞—Ö,
    –µ—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–∞–∂–∞–ª 2
    """
    for depart in depart_dic_info:
        print(f'–ù–∞–∑–≤–∞–Ω–∏–µ –¥–µ–ø–∞—Ä—Ç–∞–º–µ–Ω—Ç–∞ {depart} \nüßî–ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —á–µ–ª–æ–≤–µ–∫ –≤ –æ—Ç–¥–µ–ª–µ: {depart_dic_info[depart]["number"]}üßî')
        print(
            f'ü§ë–í–∏–ª–∫–∞ –∑–∞—Ä–ø–ª–∞—Ç —Å–æ—Ç—Ä—É–¥–Ω–∏–∫–æ–≤ –¥–µ–ø–∞—Ä—Ç–∞–º–µ–Ω—Ç–∞: {depart_dic_info[depart]["min_salary"]} - {depart_dic_info[depart]["max_salary"]}ü§ë')
        print(
            f'üëÄ–°—Ä–µ–¥–Ω—è—è –∑–∞—Ä–ø–ª–∞—Ç–∞ —Å–æ—Ç—Ä—É–¥–Ω–∏–∫–∞ –¥–µ–ø–∞—Ä—Ç–∞–º–µ–Ω—Ç–∞: {round(depart_dic_info[depart]["sum_salary"] / depart_dic_info[depart]["number"], 3)}üëÄ\n')  # –ß—Ç–æ–±—ã –ø–æ—Å—á–∏—Ç–∞—Ç—å —Å—Ä–µ–¥–Ω—é—é, –¥–µ–ª–∏–º —Å—É–º–º—É –Ω–∞ –∫–æ–ª-–≤–æ –∏ –æ–∫—Ä—É–≥–ª—è–µ–º –¥–æ 3-–µ–≥–æ –∑–Ω–∞–∫–∞


def press_three():
    """
    –§—É–Ω–∫—Ü–∏—è, –∫–æ—Ç—Ä–∞—è –∑–∞–ø–∏—Å—ã–≤–∞–µ—Ç –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –∏–∑ —Ñ—É–Ω–∫—É–∏–∏ press_two
    –≤ csv —Ñ–∞–π–ª–∏–∫
    """
    with open('depart_info.csv', mode='w', encoding='utf-8') as write_file:
        file_writer = csv.writer(write_file, delimiter=';', lineterminator='\r')
        file_writer.writerow(['–ù–∞–∑–≤–∞–Ω–∏–µ', '–ß–∏—Å–ª–µ–Ω–Ω–æ—Å—Ç—å', '–í–∏–ª–∫–∞ –ó–ü', '–°—Ä–µ–¥–Ω—è—è –∑–∞—Ä–ø–ª–∞—Ç–∞'])
        for depart in depart_dic_info:
            file_writer.writerow([depart,
                                  depart_dic_info[depart]['number'],
                                  f"{depart_dic_info[depart]['min_salary']} - {depart_dic_info[depart]['max_salary']}",
                                  round(depart_dic_info[depart]["sum_salary"] / depart_dic_info[depart]["number"], 3)]
                                 )


def start():
    """
    –§—É–Ω–∫—Ü–∏—è, —Å–æ–∑–¥–∞–Ω–Ω–∞—è –¥–ª—è –∑–∞–ø—É—Å–∫–∞ –Ω–∞—à–µ–π –ø—Ä–æ–≥—Ä–∞–º–º—ã,
    –ø—Ä–æ–≤–µ—Ä–∫–∏, –∫–∞–∫–∏–µ —á–∏—Å–ª–∞ –≤–≤–µ–ª –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å
    """
    prepare()
    print(menu)
    inp = input('–í–≤–µ–¥–∏—Ç–µ –Ω–∞ –≤—ã–±–æ—Ä —Ü–∏—Ñ—Ä—É –∏–∑ –º–µ–Ω—é \n')
    if inp not in ('1', '2', '3'):
        raise ValueError('–í—ã –≤–≤–µ–ª–∏ —Ü–∏—Ñ—Ä—É –Ω–µ –∏–∑ –º–µ–Ω—é, –ø–æ–ø—Ä–æ–±—É–π—Ç–µ –∑–∞–Ω–æ–≤–æ')  # –ï—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤–≤–µ–ª –Ω–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—É—é —Ü–∏—Ñ—Ä—É
    elif inp == '1':
        press_one()
    elif inp == '2':
        press_two()
    elif inp == '3':
        press_three()
        print('\n–§–∞–π–ª depart_info.csv —É—Å–ø–µ—à–Ω–æ —Å–æ–∑–¥–∞–Ω')


if __name__ == '__main__':
    start()
